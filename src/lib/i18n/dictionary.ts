export type Locale = 'fr' | 'en'

export const dictionaries: Record<Locale, Dictionary> = { fr, en }

/**
 * Resolve a dot-path key like 'stages.nouveau' or 'home.low_credit_message_one'
 * inside a dictionary. Returns the value (string or plural object {one, other}).
 */
export function resolveKey(dict: Dictionary, key: string): unknown {
  const parts = key.split('.')
  let current: unknown = dict
  for (const part of parts) {
    if (current && typeof current === 'object' && part in (current as Record<string, unknown>)) {
      current = (current as Record<string, unknown>)[part]
    } else {
      return undefined
    }
  }
  return current
}

/**
 * Interpolate {var} placeholders inside a string.
 */
export function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = vars[k]
    return v === undefined || v === null ? `{${k}}` : String(v)
  })
}
